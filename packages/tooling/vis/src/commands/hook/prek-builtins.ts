    "    let rc = 0;",
    "",
    "    for (const file of files) {",
    "        const isMarkdown = MD_RE.test(file);",
    "        const buf = readFileSync(file);",
    "        const out = [];",
    "        let i = 0;",
    "",
    "        while (i <= buf.length) {",
    "            let end = i;",
    "",
    "            while (end < buf.length && buf[end] !== 0x0a) {",
    "                end += 1;",
    "            }",
