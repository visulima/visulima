    "            for (const pattern of PATTERNS) {",
    "                if (line.startsWith(pattern)) {",
    "                    process.stdout.write(file + ':' + (i + 1) + ': Merge conflict string ' + JSON.stringify(pattern.trim()) + ' found\\n');",
    "                    rc = 1;",
    "                }",
    "            }",
    "        }",
    "    }",
    "",
    "    return rc;",
    "}",
