    "// ─── Entry point ────────────────────────────────────────────────────",
    "",
    "const parsed = parseArgs(process.argv.slice(2));",
    "const candidateFiles = discoverFiles(parsed.flags);",
    "const filtered = applyFilters(candidateFiles, parsed.flags);",
    "",
    "if (filtered.length === 0 && !parsed.flags.alwaysRun) {",
