    "        process.exit(2);",
    "    }",
    "    code = impl(filtered, parsed.rest);",
    "} else {",
    "    code = runCommand(parsed.rest, filtered, parsed.flags.passFilenames);",
    "}",
    "",
    "process.exit(code);",
    "",
