// Node helper script emitted into `.vis-hooks/.builtins/` during a prek → vis
// migration. It is invoked from the generated shell hooks via `node` and
// receives all user-supplied values (regex patterns, file paths, hook args) via
// process.argv — never interpolated into the shell. This keeps the migration
// output safe from command injection even if the original prek config contained
// hostile regex patterns or filenames.
//
// The runner centralises:
//   1. staged-file discovery (git diff --cached, null-separated)
//   2. file/exclude regex filters (pre-commit semantics)
//   3. types/types_or/exclude_types filters via a small pygments-derived map
//   4. chunked exec of the hook entry with filtered files as argv
//   5. dispatch to the few pre-commit-hooks built-ins we ship equivalents for
//
// The individual builtin implementations are faithful JavaScript ports of the
// Python sources in pre-commit/pre-commit-hooks (verified against upstream at
// commit time). Each builtin receives file paths as argv.

