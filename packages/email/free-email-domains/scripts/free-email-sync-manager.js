import { promises as fs } from "node:fs";
import { createRequire } from "node:module";
import { join } from "node:path";

import Semaphore from "./semaphore.js";

const require = createRequire(import.meta.url);

// Module-level regex constants to avoid re-compilation on every call
const GITHUB_URL_REGEX = /github\.com\/([^/]+)\/([^/]+)/;
const REFS_HEADS_REGEX = /^refs\/heads\//;
const HOST_FILE_REGEX = /^(?:0\.0\.0\.0\s+|127\.0\.0\.1\s+|localhost\s+)/u;
const WILDCARD_REGEX = /^\*\./u;
const TRAILING_CONTENT_REGEX = /\s\S*$/u;
const TRAILING_SEPARATOR_REGEX = /[,;][^,;]*$/u;
const EMAIL_DOMAIN_REGEX = /@([^\s@]+)$/u;
const DOMAIN_VALIDATION_REGEX = /^[a-z0-9](?:[a-z0-9-]{0,61}[a-z0-9])?(?:\.[a-z0-9](?:[a-z0-9-]{0,61}[a-z0-9])?)*$/iu;

/**
 * FreeEmailSyncManager - A manager for downloading and synchronizing free email
 * service provider domains from multiple sources (plus the local
 * `email-providers` dataset) with detailed statistics and deduplication.
 */
class FreeEmailSyncManager {
    constructor(options = {}) {
        this.domains = new Map();
        this.previousDomains = new Set();
        this.excludeDomains = new Set();
        this.syncOptions = {
            concurrency: options.concurrency ?? 5,
            outputPath: options.outputPath ?? "dist",
            retries: options.retries ?? 3,
            timeout: options.timeout ?? 30_000,
        };
    }

    /**
     * Adds a domain to the collection with source tracking.
     * @param {string} domain The domain to add.
     * @param {string} source The source repository URL.
     */
    addDomain(domain, source) {
        const normalizedDomain = domain.toLowerCase().trim();
        const now = new Date().toISOString();

        if (this.domains.has(normalizedDomain)) {
            const entry = this.domains.get(normalizedDomain);

            entry.sources.add(source);
            entry.lastSeen = now;
        } else {
            this.domains.set(normalizedDomain, {
                domain: normalizedDomain,
                firstSeen: now,
                lastSeen: now,
                sources: new Set([source]),
            });
        }
    }

    /**
     * Builds detailed markdown report.
     * @param {object} stats Statistics object.
     * @returns {string} Markdown report content.
     */
    // eslint-disable-next-line class-methods-use-this -- Method uses stats parameter, not instance state
    buildDetailedReport(stats) {
        const successRate = ((stats.successfulDownloads / stats.totalRepositories) * 100).toFixed(1);

        return `# Free Email Domains Sync Report

## Summary

- **Last Sync**: ${stats.lastSyncTimestamp}
- **Processing Time**: ${(stats.processingTime / 1000).toFixed(2)}s
- **Total Repositories**: ${stats.totalRepositories}
- **Successful Downloads**: ${stats.successfulDownloads}
- **Failed Downloads**: ${stats.failedDownloads}
- **Success Rate**: ${successRate}%

## Domain Statistics

- **Total Unique Domains**: ${stats.uniqueDomains.toLocaleString()}
- **New Domains**: ${stats.newDomains.toLocaleString()}
- **Removed Domains**: ${stats.removedDomains.toLocaleString()}
- **Duplicate Entries Found**: ${stats.duplicates.toLocaleString()}

## Repository Details

${Array.from(
    stats.repositoryStats.values(),
    (repo) => `
### ${repo.url}

- **Status**: ${repo.success ? "✅ Success" : "❌ Failed"}
- **Domains**: ${repo.domainsCount.toLocaleString()}
- **Download Time**: ${repo.downloadTime}ms
- **File Size**: ${repo.fileSize.toLocaleString()} bytes

${repo.error ? `- **Error**: ${repo.error}` : ""}
`,
).join("\n")}

---

*Generated by FreeEmailSyncManager*
`;
    }

    /**
     * Calculates final statistics after processing.
     * @param {object} stats Statistics object to update.
     * @param {number} startTime Start time timestamp.
     */
    calculateFinalStats(stats, startTime) {
        // Use the published (exclude-filtered) set so report counts and
        // new/removed deltas match what actually lands in domains.json.
        const currentDomains = new Set(this.getPublishedDomains());
        const duplicates = [...this.domains.values()].reduce((sum, entry) => sum + (entry.sources.size - 1), 0);
        const newDomains = [...currentDomains].filter((domain) => !this.previousDomains.has(domain)).length;
        const removedDomains = [...this.previousDomains].filter((domain) => !currentDomains.has(domain)).length;
        const processingTime = Date.now() - startTime;
        const lastSyncTimestamp = new Date().toISOString();

        Object.assign(stats, {
            duplicates,
            lastSyncTimestamp,
            newDomains,
            processingTime,
            removedDomains,
            totalDomains: currentDomains.size,
            uniqueDomains: currentDomains.size,
        });
    }

    /**
     * Converts GitHub repository URL to raw file URL.
     * @param {string} repoUrl The GitHub repository URL.
     * @param {string} filePath The file path within the repository.
     * @returns {string} The raw GitHub file URL.
     */
    // eslint-disable-next-line class-methods-use-this -- Utility method, doesn't need instance state
    convertToRawGitHubUrl(repoUrl, filePath) {
        const match = repoUrl.match(GITHUB_URL_REGEX);

        if (!match) {
            throw new Error(`Invalid GitHub URL: ${repoUrl}`);
        }

        const owner = match[1];
        const repo = match[2];
        let cleanFilePath = filePath.startsWith("/") ? filePath.slice(1) : filePath;

        // Handle /refs/heads/ prefix - convert to branch name
        // e.g., /refs/heads/main/file.txt -> main/file.txt
        if (cleanFilePath.startsWith("refs/heads/")) {
            cleanFilePath = cleanFilePath.replace(REFS_HEADS_REGEX, "");
        }

        return `https://raw.githubusercontent.com/${owner}/${repo}/${cleanFilePath}`;
    }

    /**
     * Simple delay utility for retry backoff.
     * @param {number} ms Milliseconds to delay.
     * @returns {Promise<void>} Promise that resolves after the delay.
     */
    // eslint-disable-next-line class-methods-use-this -- Utility method, doesn't need instance state
    delay(ms) {
        return new Promise((resolve) => {
            setTimeout(resolve, ms);
        });
    }

    /**
     * Downloads domains from all repositories with controlled concurrency.
     * @param {Array<object>} repositories Array of repository configurations.
     * @returns {Promise<Array<object>>} Array of download results.
     */
    async downloadFromRepositories(repositories) {
        const results = [];
        const semaphore = new Semaphore(this.syncOptions.concurrency);

        const downloadPromises = repositories.map(async (repo) => semaphore.acquire(async () => this.downloadFromRepository(repo)));

        const downloadResults = await Promise.allSettled(downloadPromises);

        downloadResults.forEach((result, index) => {
            if (result.status === "fulfilled") {
                results.push(result.value);
            } else {
                results.push({
                    domainsCount: 0,
                    downloadTime: 0,
                    error: result.reason?.message ?? "Unknown error",
                    fileSize: 0,
                    success: false,
                    url: repositories[index].url,
                });
            }
        });

        return results;
    }

    /**
     * Downloads domains from a single repository with retry logic.
     * @param {object} repo Repository configuration.
     * @returns {Promise<object>} Download result object.
     */
    async downloadFromRepository(repo) {
        const startTime = Date.now();
        let lastError;

        const attempts = Array.from({ length: this.syncOptions.retries }, (_, i) => i);

        for (const attempt of attempts) {
            try {
                // eslint-disable-next-line no-await-in-loop -- Retry logic requires sequential attempts
                const domains = await this.fetchDomainsFromRepository(repo);
                const downloadTime = Date.now() - startTime;

                // Add domains to our collection
                domains.forEach((domain) => {
                    this.addDomain(domain, repo.url);
                });

                return {
                    domainsCount: domains.length,
                    downloadTime,
                    fileSize: domains.join("\n").length,
                    success: true,
                    url: repo.url,
                };
            } catch (error) {
                lastError = error;

                if (attempt < this.syncOptions.retries - 1) {
                    // eslint-disable-next-line no-await-in-loop -- Exponential backoff requires sequential delays
                    await this.delay(2 ** attempt * 1000);
                }
            }
        }

        return {
            domainsCount: 0,
            downloadTime: Date.now() - startTime,
            error: lastError?.message ?? "Unknown error",
            fileSize: 0,
            success: false,
            url: repo.url,
        };
    }

    /**
     * Extracts domain from various line formats.
     * @param {string} line The line to extract domain from.
     * @returns {string|undefined} Extracted domain or undefined.
     */
    // eslint-disable-next-line class-methods-use-this -- Utility method, doesn't need instance state
    extractDomain(line) {
        // Remove common prefixes and suffixes
        // Using safer regex patterns to avoid backtracking issues
        const cleanedLine = line
            .replace(HOST_FILE_REGEX, "") // Host file format
            .replace(WILDCARD_REGEX, "") // Wildcard format
            .replace(TRAILING_CONTENT_REGEX, "") // Remove trailing content (safer pattern)
            .replace(TRAILING_SEPARATOR_REGEX, "") // Remove trailing separators (safer pattern)
            .trim();

        // Extract email domain from email format
        const emailMatch = cleanedLine.match(EMAIL_DOMAIN_REGEX);

        if (emailMatch) {
            return emailMatch[1];
        }

        return cleanedLine || undefined;
    }

    /**
     * Fetches domains from a repository based on its type.
     * @param {object} repo Repository configuration.
     * @returns {Promise<Array<string>>} Array of domain strings.
     */
    async fetchDomainsFromRepository(repo) {
        switch (repo.type) {
            case "api": {
                return this.fetchFromApi(repo);
            }

            case "git": {
                return this.fetchFromGitRepository(repo);
            }

            case "raw": {
                return this.fetchFromRawUrl(repo);
            }

            default: {
                throw new Error(`Unsupported repository type: ${repo.type}`);
            }
        }
    }

    /**
     * Fetches domains from an API endpoint.
     * @param {object} repo Repository configuration.
     * @returns {Promise<Array<string>>} Array of domain strings.
     */
    async fetchFromApi(repo) {
        const controller = new AbortController();
        const timeoutId = setTimeout(() => {
            controller.abort();
        }, this.syncOptions.timeout);

        try {
            const response = await fetch(repo.url, {
                headers: {
                    Accept: "application/json",
                    "User-Agent": "FreeEmailSyncManager/1.0",
                },
                signal: controller.signal,
            });

            if (!response.ok) {
                throw new Error(`HTTP ${response.status}: ${response.statusText}`);
            }

            const data = await response.json();

            // Handle different API response formats. Every path runs through the
            // same normalize + validate pipeline (trim/lowercase/isValidDomain)
            // as the raw-text and email-providers sources, so API-fed domains are
            // never persisted unvalidated.
            if (Array.isArray(data)) {
                return this.normalizeApiDomains(data);
            }

            if (data.domains && Array.isArray(data.domains)) {
                return this.normalizeApiDomains(data.domains);
            }

            if (typeof data === "object") {
                return this.normalizeApiDomains(Object.keys(data));
            }

            throw new Error("Unsupported API response format");
        } finally {
            clearTimeout(timeoutId);
        }
    }

    /**
     * Fetches domains from GitHub repository using raw file URLs.
     * @param {object} repo Repository configuration.
     * @returns {Promise<Array<string>>} Array of domain strings.
     */
    async fetchFromGitRepository(repo) {
        const allDomains = [];
        const filePaths = repo.blocklist_files || [];

        const domainPromises = filePaths.map(async (filePath) => {
            const rawUrl = this.convertToRawGitHubUrl(repo.url, filePath);

            return this.fetchFromRawUrl({ ...repo, url: rawUrl });
        });

        const domainArrays = await Promise.all(domainPromises);

        for (const domains of domainArrays) {
            allDomains.push(...domains);
        }

        return allDomains;
    }

    /**
     * Fetches domains from a raw text URL.
     * @param {object} repo Repository configuration.
     * @returns {Promise<Array<string>>} Array of domain strings.
     */
    async fetchFromRawUrl(repo) {
        const controller = new AbortController();
        const timeoutId = setTimeout(() => {
            controller.abort();
        }, this.syncOptions.timeout);

        try {
            const response = await fetch(repo.url, {
                headers: {
                    "User-Agent": "FreeEmailSyncManager/1.0",
                },
                signal: controller.signal,
            });

            if (!response.ok) {
                throw new Error(`HTTP ${response.status}: ${response.statusText}`);
            }

            const text = await response.text();

            return this.parseDomainList(text);
        } finally {
            clearTimeout(timeoutId);
        }
    }

    /**
     * Generates detailed human-readable report.
     * @param {object} stats Statistics object.
     * @returns {string} Report content as string.
     */
    generateDetailedReport(stats) {
        return this.buildDetailedReport(stats);
    }

    /**
     * Generates the main domains list file (JSON format only).
     *
     * Emits every collected domain, sorted and de-duplicated, minus anything
     * present in the exclude list (`scripts/config/blacklist.json`).
     */
    async generateDomainsList() {
        const domainsArray = this.getPublishedDomains();

        await fs.writeFile(join(this.syncOptions.outputPath, "domains.json"), JSON.stringify(domainsArray), "utf8");

        // Emit a type declaration so the `./domains` subpath export carries types.
        // Use a default export to match the JSON module's default export, so
        // `import domains from "@visulima/free-email-domains/domains" with { type: "json" }`
        // type-checks (an `export =` would force `import domains = require(...)`).
        await fs.writeFile(join(this.syncOptions.outputPath, "domains.d.ts"), "declare const domains: string[];\nexport default domains;\n", "utf8");
    }

    /**
     * Generates all output files and statistics.
     */
    async generateOutputs() {
        await fs.mkdir(this.syncOptions.outputPath, { recursive: true });

        // Generate main domains list
        await this.generateDomainsList();
    }

    /**
     * Returns the published domain set: every collected domain minus anything in
     * the exclude list (`scripts/config/blacklist.json`), sorted and de-duplicated.
     * This is the single source of truth shared by the generated `domains.json`,
     * the final statistics, and the value returned from {@link sync}, so excluded
     * domains never leak into report counts or the returned set while being absent
     * from the file.
     * @returns {string[]} The sorted, exclude-filtered domain list.
     */
    getPublishedDomains() {
        return [...this.domains.keys()].filter((domain) => !this.excludeDomains.has(domain.toLowerCase().trim())).toSorted();
    }

    /**
     * Initializes statistics object.
     * @param {number} repositoryCount Number of repositories.
     * @returns {object} Initialized statistics object.
     */
    // eslint-disable-next-line class-methods-use-this -- Factory method, doesn't need instance state
    initializeStats(repositoryCount) {
        return {
            duplicates: 0,
            excludeDomains: 0,
            failedDownloads: 0,
            lastSyncTimestamp: "",
            newDomains: 0,
            processingTime: 0,
            providerDomains: 0,
            removedDomains: 0,
            repositoryStats: new Map(),
            successfulDownloads: 0,
            totalDomains: 0,
            totalRepositories: repositoryCount,
            uniqueDomains: 0,
        };
    }

    /**
     * Validates domain format using regex.
     * @param {string} domain The domain to validate.
     * @returns {boolean} True if domain is valid.
     */
    // eslint-disable-next-line class-methods-use-this -- Utility method, doesn't need instance state
    isValidDomain(domain) {
        if (!domain || domain.length > 253) {
            return false;
        }

        return DOMAIN_VALIDATION_REGEX.test(domain) && !domain.includes("..") && domain.includes(".");
    }

    /**
     * Loads the bundled `email-providers` dataset (common + all free providers)
     * and adds it to the collection. This is the deterministic base source, so
     * the list is usable even when every network source fails.
     * @returns {number} Number of provider domains added.
     */
    loadEmailProviders() {
        let added = 0;

        const ingest = (data) => {
            if (Array.isArray(data)) {
                data.forEach((domain) => {
                    if (typeof domain === "string" && domain) {
                        const normalizedDomain = domain.trim().toLowerCase();

                        if (normalizedDomain && this.isValidDomain(normalizedDomain)) {
                            this.addDomain(normalizedDomain, "email-providers");
                            added += 1;
                        }
                    }
                });
            }
        };

        try {
            ingest(require("email-providers/common.json"));
            ingest(require("email-providers/all.json"));
        } catch {
            // If email-providers is not available, continue without it.
        }

        // eslint-disable-next-line no-console
        console.log(`📦 Loaded ${added} domains from the email-providers dataset`);

        return added;
    }

    /**
     * Loads the exclude list (`blacklist.json`) of domains that should never be
     * published as free providers (corporate domains that occasionally leak into
     * upstream free lists). These are filtered out at output time.
     * @param {string} excludePath Path to the exclude list JSON file.
     * @returns {Promise<number>} Number of domains added to the exclude set.
     */
    async loadExcludeList(excludePath) {
        try {
            const content = await fs.readFile(excludePath, "utf8");
            const excludeData = JSON.parse(content);

            if (Array.isArray(excludeData)) {
                let addedCount = 0;

                excludeData.forEach((domain) => {
                    if (typeof domain === "string" && domain) {
                        const normalizedDomain = domain.trim().toLowerCase();

                        if (normalizedDomain) {
                            this.excludeDomains.add(normalizedDomain);
                            addedCount += 1;
                        }
                    }
                });

                // eslint-disable-next-line no-console
                console.log(`📋 Loaded ${addedCount} domains from the exclude list`);

                return addedCount;
            }

            // eslint-disable-next-line no-console
            console.warn("⚠️  blacklist.json is not an array, skipping");

            return 0;
        } catch (error) {
            if (error.code === "ENOENT") {
                // File doesn't exist, which is fine - the exclude list is optional
                // eslint-disable-next-line no-console
                console.log("ℹ️  No blacklist.json found, skipping exclude list");
            } else {
                // eslint-disable-next-line no-console
                console.warn(`⚠️  Error loading blacklist.json: ${error.message}`);
            }

            return 0;
        }
    }

    /**
     * Loads previous domain list for comparison
     */
    async loadPreviousDomains() {
        try {
            const filePath = join(this.syncOptions.outputPath, "domains.json");
            const content = await fs.readFile(filePath, "utf8");
            const domainsData = JSON.parse(content);

            if (Array.isArray(domainsData)) {
                domainsData.forEach((domain) => {
                    if (typeof domain === "string" && domain) {
                        this.previousDomains.add(domain.trim().toLowerCase());
                    }
                });
            }
        } catch {
            // File doesn't exist, which is fine for first run
        }
    }

    /**
     * Normalizes and validates a list of domain strings coming from a JSON API
     * source, mirroring the raw-text/email-providers pipeline: trim, lowercase,
     * and drop anything that fails `isValidDomain`.
     * @param {Array<unknown>} list Raw values from the API response.
     * @returns {Array<string>} Normalized, validated domain strings.
     */
    normalizeApiDomains(list) {
        const domains = [];

        for (const item of list) {
            if (typeof item !== "string") {
                continue;
            }

            const normalizedDomain = item.trim().toLowerCase();

            if (normalizedDomain && this.isValidDomain(normalizedDomain)) {
                domains.push(normalizedDomain);
            }
        }

        return domains;
    }

    /**
     * Parses domain list from text content with various formats support.
     * @param {string} text The text content to parse.
     * @returns {Array<string>} Array of valid domain strings.
     */
    parseDomainList(text) {
        const domains = [];
        const lines = text.split("\n");

        for (const originalLine of lines) {
            const trimmedLine = originalLine.trim();

            // Skip empty lines and comments
            if (!trimmedLine || trimmedLine.startsWith("#") || trimmedLine.startsWith("//") || trimmedLine.startsWith(";")) {
                continue;
            }

            // Extract domain from various formats
            const domain = this.extractDomain(trimmedLine);

            if (domain && this.isValidDomain(domain)) {
                domains.push(domain.toLowerCase());
            }
        }

        return domains;
    }

    /**
     * Processes download results and updates statistics.
     * @param {Array<object>} results Array of download results.
     * @param {object} stats Statistics object to update.
     */
    // eslint-disable-next-line class-methods-use-this -- Method processes results, doesn't need instance state
    processDownloadResults(results, stats) {
        const statsCopy = { ...stats };

        results.forEach((result) => {
            statsCopy.repositoryStats.set(result.url, result);

            if (result.success) {
                statsCopy.successfulDownloads += 1;
            } else {
                statsCopy.failedDownloads += 1;
            }
        });

        Object.assign(stats, statsCopy);
    }

    /**
     * Main synchronization method that orchestrates the entire process.
     * @param {Array<object>} repositories Array of repository configurations.
     * @param {string} [excludePath] Optional path to the exclude list JSON file.
     * @returns {Promise<object>} Sync result with domains, errors, and stats.
     */
    async sync(repositories, excludePath) {
        const startTime = Date.now();
        const stats = this.initializeStats(repositories.length);
        const errors = [];

        try {
            // Load previous domains for comparison
            await this.loadPreviousDomains();

            // Always seed from the deterministic local email-providers dataset.
            stats.providerDomains = this.loadEmailProviders();

            // Download from all repositories with concurrency control
            const downloadResults = await this.downloadFromRepositories(repositories);

            // Process results and update stats
            this.processDownloadResults(downloadResults, stats);

            // Load the exclude list if provided
            if (excludePath) {
                stats.excludeDomains = await this.loadExcludeList(excludePath);
            }

            // Calculate final statistics
            this.calculateFinalStats(stats, startTime);

            // Generate outputs
            await this.generateOutputs();

            return {
                domains: new Set(this.getPublishedDomains()),
                errors,
                stats,
            };
        } catch (error) {
            errors.push(error);

            throw error;
        }
    }
}

export default FreeEmailSyncManager;
